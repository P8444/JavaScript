var num1=5;
var num1=num1+1;
console.log(`${num1}`);

var num2=10;
var num2=num2++;//n2+1=11
console.log(`Post increment operator:${num2}`);
 
var num3=15;
var num3=++num3;//n3+1=16
console.log(`Prefix increment operator:${num3}`);
 
var num4=15;
var num4=num4--;
console.log(`Post decrement operator:${num4}`);

var num5=30;
var num5=--num5;
console.log(`Prefix decrement operator:${num5}`);

